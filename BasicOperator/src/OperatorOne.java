
public class OperatorOne {
	public static void main(String[] args){
		
		int number1 = 11; 
		int number2 = 21; 
		int number3 = 31; 
		int number4 = 41; 
		int number5 = 51;
		
		number1 = number1 * 5;
		number2 = number2 * 5;
		number3 = number3 * 5;
		number4 = number4 * 5;
		number5 = number5 * 5;
		
		System.out.println(number1);
		System.out.println(number2);
		System.out.println(number3);
		System.out.println(number4);
		System.out.println(number5);
		
		number1 *= 2; 
		number2 *= 2;
		number3 *= 2; 
		number4 *= 2;
		number5 *= 2;
		
		System.out.println(number1);
		System.out.println(number2);
		System.out.println(number3);
		System.out.println(number4);
		System.out.println(number5);
		

		System.out.println(5%2);
		
		//문자열 생성 
		String str = "안녕하세";
		str = str + "요"; //+ 는 정수, 실수만 더해 주는 것이 아니라 문자열도 더할 수 있다
		str += ".";
		
		System.out.println(str);
		
		//비교연산자 : > >= < <= == !=(not)
		
		
		
	}

}
